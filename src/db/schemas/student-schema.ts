import { Schema, model } from "mongoose"

const studentSchema = new Schema({
  nama: { type: String, required: true },
  NIPD: { type: String, default: null },
  jenis_kelamin: { type: String, enum: ["male", "female"], required: true },
  NISN: { type: String, required: true },
  tempat_lahir: { type: String, required: true },
  NIK: { type: String, required: true },
  agama: { type: String, required: true, enum: ['Islam', 'Kristen', 'Katolik', 'Hindu', 'Buddha'] },
  alamat_jalan: { type: String, required: true },
  RT: { type: Number, required: true },
  RW: { type: Number, required: true },
  dusun: { type: String, required: true },
  desa_kelurahan: { type: String, required: true },
  kecamatan: { type: String, required: true },
  kode_pos: { type: String, default: null },
  jenis_tinggal: { type: String, required: true, enum: ['Bersama orang tua', 'lainnya'] },
  alat_transportasi: { type: String, required: true },
  telepon: { type: String, default: null },
  no_hp: { type: String, default: null },
  email: { type: String, default: null },
  SKHUN: { type: String, default: null },
  penerima_KPS: { type: Boolean, required: true },
  no_KPS: { type: String, default: null },
  rombel_saat_ini: { type: String, required: true },
  no_peserta_ujian_nasional: { type: String, default: null },
  no_seri_ijazah: { type: String, default: null },
  penerima_KIP: { type: Boolean, required: true },
  no_KIP: { type: String, default: null },
  nama_di_KIP: { type: String, default: null },
  no_registrasi_akta_lahir: { type: String, default: null },
  bank: { type: String, default: null },
  no_rekening: { type: String, default: null },
  rekening_atas_nama: { type: String, default: null },
  layak_PIP: { type: Boolean, required: true },
  alasan_layak_PIP: { type: String, default: null },
  kebutuhan_khusus: { type: String, default: null },
  sekolah_asal: { type: String, default: null },
  anak_ke: { type: Number, required: true },
  lintang: { type: String, default: null },
  bujur: { type: String, default: null },
  berat_badan: { type: Number, required: true },
  tinggi_badan: { type: Number, required: true },
  lingkar_kepala: { type: Number, default: 0 },
  jumlah_saudara_kandung: { type: String, default: 0 },
  jarak_rumah_ke_sekolah_KM: { type: Number, default: 0 },
})

export const Student = model("students", studentSchema)
